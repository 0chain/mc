package main

import (
	"bytes"
	"fmt"
	"os"
	"os/exec"
	"strconv"
	"text/template"
	"time"

	"github.com/minio/cli"
)

type Version struct {
	Date string
	Tag  string
}

func writeVersion(version Version) error {
	var versionTemplate = `// --------  DO NOT EDIT --------
// this is an autogenerated file

package main

import (
	"net/http"
	"time"
)

// Version autogenerated
var Version = {{if .Date}}"{{.Date}}"{{else}}""{{end}}

// Tag is of following format
//
//   [STRING]-[EPOCH]
//
// STRING is release string of your choice.
// EPOCH is unix seconds since Jan 1, 1970 UTC.
var Tag = {{if .Tag}}"{{.Tag}}"{{else}}""{{end}}

// getVersion -
func getVersion() string {
	t, _ := time.Parse(time.RFC3339Nano, Version)
	if t.IsZero() {
		return ""
	}
	return t.Format(http.TimeFormat)
}
`
	t := template.Must(template.New("version").Parse(versionTemplate))
	versionFile, err := os.OpenFile("version.go", os.O_WRONLY|os.O_CREATE|os.O_TRUNC, 0600)
	if err != nil {
		return err
	}
	defer versionFile.Close()
	err = t.Execute(versionFile, version)
	if err != nil {
		return err
	}
	return nil
}

type command struct {
	cmd    *exec.Cmd
	stderr *bytes.Buffer
	stdout *bytes.Buffer
}

func (c command) runCommand() error {
	c.cmd.Stdout = c.stdout
	c.cmd.Stderr = c.stderr
	return c.cmd.Run()
}
func (c command) String() string {
	message := c.stderr.String()
	message += c.stdout.String()
	return message
}

func runMcInstall(ctx *cli.Context) {
	if ctx.Args().First() == "help" {
		cli.ShowCommandHelpAndExit(ctx, "install", 1) // last argument is exit code
	}
	mcBuild := command{exec.Command("godep", "go", "build", "-a", "./..."), &bytes.Buffer{}, &bytes.Buffer{}}
	mcTest := command{exec.Command("godep", "go", "test", "-race", "./..."), &bytes.Buffer{}, &bytes.Buffer{}}
	mcInstall := command{exec.Command("godep", "go", "install", "-a", "github.com/minio/mc"), &bytes.Buffer{}, &bytes.Buffer{}}
	mcBuildErr := mcBuild.runCommand()
	if mcBuildErr != nil {
		fmt.Print(mcBuild)
		os.Exit(1)
	}
	fmt.Print(mcBuild)
	mcTestErr := mcTest.runCommand()
	if mcTestErr != nil {
		fmt.Println(mcTest)
		os.Exit(1)
	}
	fmt.Print(mcTest)
	mcInstallErr := mcInstall.runCommand()
	if mcInstallErr != nil {
		fmt.Println(mcInstall)
		os.Exit(1)
	}
	fmt.Print(mcInstall)
}

func runMcRelease(ctx *cli.Context) {
	if ctx.Args().First() == "help" {
		cli.ShowCommandHelpAndExit(ctx, "release", 1) // last argument is exit code
	}
	t := time.Now().UTC()
	date := t.Format(time.RFC3339Nano)
	//   [STRING]-[EPOCH]
	//
	// STRING is release string of your choice.
	// EPOCH is unix seconds since Jan 1, 1970 UTC.
	tag := "release" + "-" + strconv.FormatInt(t.Unix(), 10)

	version := Version{Date: date, Tag: tag}
	err := writeVersion(version)
	if err != nil {
		fmt.Print(err)
		os.Exit(1)
	}
}

func main() {
	app := cli.NewApp()
	app.Usage = "Minio Client for object storage and filesystems"
	app.Commands = []cli.Command{
		{
			Name:   "release",
			Action: runMcRelease,
		},
		{
			Name:   "install",
			Action: runMcInstall,
		},
	}
	app.Author = "Minio.io"
	app.RunAndExitOnError()
}
